namespace Domain.Enums;

/// <summary>
/// Represents the digit options available in IVR menus
/// </summary>
public enum MenuOption
{
    /// <summary>
    /// Option 1 - Navigate to first submenu or action
    /// </summary>
    Option1 = 1,

    /// <summary>
    /// Option 2 - Navigate to second submenu or action
    /// </summary>
    Option2 = 2,

    /// <summary>
    /// Option 3 - Navigate to third submenu or action
    /// </summary>
    Option3 = 3,

    /// <summary>
    /// Option 4 - Return to previous menu (back navigation)
    /// </summary>
    Option4 = 4
}
